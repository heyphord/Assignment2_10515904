import java.util.Scanner;
public class ex48 {
    public static void main(String[] args)
    {
        /*
        1) They all evaluate a conditional statement before they execute the code in their body
        2) The while loop execute the code over again until the condition evaluates to false
        3) 'entry' has already been decleared as an integer
        */
        Scanner keyboard = new Scanner(System.in);
		int pin = 12345;

		System.out.println("WELCOME TO THE BANK OF MITCHELL.");
		System.out.print("ENTER YOUR PIN: ");
		int entry = keyboard.nextInt();

		while ( entry != pin )
		{
			System.out.println("\nINCORRECT PIN. TRY AGAIN.");
			System.out.print("ENTER YOUR PIN: ");
			entry = keyboard.nextInt();
		}

		System.out.println("\nPIN ACCEPTED. YOU NOW HAVE ACCESS TO YOUR ACCOUNT.");
    }
    
}
